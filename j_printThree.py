def printThree():
    print(0)
    print(1)
    print(2)
    print() #blank space

def printThreeArray(array):
    print(array[0])
    print(array[1])
    print(array[2])
    print() #blank space


printThree()
printThreeArray([4,5,6])
printThreeArray(["foo","bar","baz"])
printThreeArray(["foo","bar","baz", "buzz", "bang"])